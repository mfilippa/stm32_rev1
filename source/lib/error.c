// private includes
#include "error.h"

// module struct
struct error_struct {
    error_t error;
    error_handler_t * handler;
} error;


// -----------------------------------------------------------------------------
// init
// -----------------------------------------------------------------------------
void error_init(error_handler_t * handler){
    error.handler = handler;
}

// -----------------------------------------------------------------------------
// raise error
// -----------------------------------------------------------------------------
void error_raise(error_t err){
    if (error.handler!=NULL) (*error.handler)(err);
}
